import React, { useState } from "react";
import Head from "next/head";
import Navbar from "../components/Navbar";

import FirstSection from "../sections/firstSection/FirstSection";
import { SecondSection } from "../sections/secondSection/SecondSection";
import { ThirdSection } from "../sections/thirdSection/ThirdSection";
import { FormSection } from "../sections/FormSection/FormSection";
import { TopArrow } from "../components/TopArrow";

// import Menu from "../sections/menu/Menu";
import { useRouter } from "next/router";
import { Button } from "@mui/material";

import { ru, en } from "../translations";

const siteTitle = "GreenGo";

export default function Home({ title = siteTitle }) {
  const [isMenuOpen, setMenuOpen] = useState(false);
  const closeMenu = () => setMenuOpen(false);
  const openMenu = () => setMenuOpen(true);

  const router = useRouter();
  const { locale } = router;
  const t = locale === "ru" ? "ru" : "en";

  const handelLanguageToggle = () => {
    switch (locale) {
      case "ru":
        router.push("/", "/", { locale: "en" });
        break;
      case "en":
        router.push("/", "/", { locale: "ru" });
        break;
    }
  };

  return (
    <>
      <Head>
        <title>{title} | GreenGo </title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Navbar
        onMenuIconClick={openMenu}
        close={closeMenu}
        isOpen={isMenuOpen}
      />
      <div className="o-scroll" id="js-scroll" data-scroll-container>
        <FirstSection />

        <SecondSection />

        <section className={{ textAlign: "center" }} data-scroll-section>
          <h1>welcome = {t.welcome}</h1>

          <Button variant="contained" onClick={() => handelLanguageToggle()}>
            1111 {locale} 1111
          </Button>
        </section>

        <ThirdSection />

        <FormSection />

        <TopArrow />
      </div>
    </>
  );
}
